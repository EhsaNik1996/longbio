export async function fetchJobPositions(): Promise<JobPosition[]> {
  await new Promise((resolve) => setTimeout(resolve, 300))
  return jobPositions
}

export async function fetchCompaniesForPosition(positionId: string): Promise<string[]> {
  await new Promise((resolve) => setTimeout(resolve, 200))

  const position = jobPositions.find((p) => p.id === positionId)
  return position ? position.companies : []
}
